CREATE TABLE GRUPO(
    NUMERO_GRUPO INT NOT NULL PRIMARY KEY,
    CANTESTUDIANTES INT
);

CREATE TABLE FACULTAD(
    ID_FACULTAD INT NOT NULL PRIMARY KEY,
    NOMBRE VARCHAR(1000) NOT NULL,
    DECANO VARCHAR(1000) NOT NULL
);

CREATE TABLE PROFESOR(
    ID_PROFESOR INT NOT NULL PRIMARY KEY,
    TELEFONO INT,
    CORREO VARCHAR(1000),
    PRIMER_NOMBRE VARCHAR(100) NOT NULL,
    SEGUNDO_NOMBRE VARCHAR(100),
    APELLIDOS VARCHAR(100) NOT NULL
);

CREATE TABLE ESTUDIANTE(
    CODIGO_ESTUDIANTE INT NOT NULL PRIMARY KEY,
    PRIMER_NOMBRE VARCHAR(100) NOT NULL,
    SEGUNDO_NOMBRE VARCHAR(100),
    APELLIDOS VARCHAR(100) NOT NULL,
    NUMERO_GRUPO INT REFERENCES GRUPO(NUMERO_GRUPO)
);

CREATE TABLE PROFESOR_ESTUDIANTE(
    CANTIDAD_ALUMNOS INT,
    SECCION INT NOT NULL,
    ID_PROFESOR INT NOT NULL REFERENCES PROFESOR(ID_PROFESOR),
    CODIGO_ESTUDIANTE INT REFERENCES ESTUDIANTE(CODIGO_ESTUDIANTE)
);

CREATE TABLE CURSO(
    CODIGO_CURSO INT NOT NULL PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL
);

CREATE TABLE CARRERA(
    ID_CARRERA INT NOT NULL PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    DURACION VARCHAR(100) NOT NULL,
    ID_FACULTAD INT REFERENCES FACULTAD(ID_FACULTAD)
);

CREATE TABLE CARRERA_CURSO(
    ES_OBLIGATORIO NUMBER(1) NOT NULL,
    CREDITOS INT NOT NULL,
    ID_CARRERA INT REFERENCES CARRERA(ID_CARRERA),
    CODIGO_CURSO INT REFERENCES CURSO(CODIGO_CURSO)
);

CREATE TABLE TRABAJO_INVEST(
    ID_TRABAJO INT NOT NULL PRIMARY KEY,
    TITULO VARCHAR(1000),
    OBJETIVO VARCHAR(1000),
    PERIODO VARCHAR(100),
    NUMERO_GRUPO INT REFERENCES GRUPO(NUMERO_GRUPO),
    CODIGO_CURSO INT REFERENCES CURSO(CODIGO_CURSO),
    ID_PROFESOR INT REFERENCES PROFESOR(ID_PROFESOR)
);

CREATE TABLE AVANCE(
    FECHA_ENTREGA DATE,
    FECHA_REVISION DATE,
    NOTA INT,
    ESTADO VARCHAR(100),
    COMENTARIO VARCHAR(2000),
    NUMERO_AVANCE INT,
    NUMERO_GRUPO INT REFERENCES GRUPO(NUMERO_GRUPO),
    ID_PROFESOR INT REFERENCES PROFESOR(ID_PROFESOR),
    ID_TRABAJO INT REFERENCES TRABAJO_INVEST(ID_TRABAJO)
);

CREATE TABLE PROPUESTA(
    TEMA VARCHAR(1000),
    DESCRIPCION VARCHAR(4000),
    NUMERO_GRUPO INT REFERENCES GRUPO(NUMERO_GRUPO),
    ID_TRABAJO INT REFERENCES TRABAJO_INVEST(ID_TRABAJO)
);

CREATE SEQUENCE PROFESOR_SEQ 
START WITH 1 
INCREMENT BY 1 
NOCACHE;

CREATE OR REPLACE TRIGGER PROFESOR_TRIGGER
BEFORE INSERT ON PROFESOR
FOR EACH ROW
BEGIN
    :NEW.ID_PROFESOR := PROFESOR_SEQ.NEXTVAL;
END;

CREATE SEQUENCE GRUPO_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE;

CREATE OR REPLACE TRIGGER GRUPO_TRIGGER
BEFORE INSERT ON GRUPO
FOR EACH ROW
BEGIN
    :NEW.NUMERO_GRUPO := GRUPO_SEQ.NEXTVAL;
END;

CREATE SEQUENCE TRAB_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE;

CREATE OR REPLACE TRIGGER TRAB_TRIGGER
BEFORE INSERT ON TRABAJO_INVEST
FOR EACH ROW
BEGIN
    :NEW.ID_TRABAJO := TRAB_SEQ.NEXTVAL;
END;

