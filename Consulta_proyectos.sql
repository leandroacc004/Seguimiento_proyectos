CREATE OR REPLACE PROCEDURE Validar_Profesor_Login (
    correo_ingresado IN VARCHAR2,
    resultado OUT VARCHAR2
) AS
    profesor_existente INT;
BEGIN
    SELECT COUNT(*)
    INTO profesor_existente
    FROM PROFESOR
    WHERE CORREO = correo_ingresado;

    IF profesor_existente > 0 THEN
        resultado := 'Acceso Permitido';
    ELSE
        resultado := 'Acceso Denegado';
    END IF;
END Validar_Profesor_Login;


CREATE OR REPLACE PROCEDURE Registrar_Revision_Avance (
    avance_id IN INT,
    fecha_revision IN DATE,
    nuevo_estado IN VARCHAR2,
    comentario IN VARCHAR2
) AS
BEGIN
    UPDATE AVANCE
    SET FECHA_REVISION = fecha_revision,
        ESTADO = nuevo_estado,
        COMENTARIO = comentario
    WHERE NUMERO_AVANCE = avance_id;
COMMIT;
END;


CREATE OR REPLACE PROCEDURE Asignar_Nota (
    avance_id IN INT,
    nota IN INT
) AS
BEGIN
    UPDATE AVANCE
    SET NOTA = nota
    WHERE NUMERO_AVANCE = avance_id;
END;


CREATE OR REPLACE FUNCTION Listar_Cursos_Secciones (
    profesor_id IN INT
) RETURN SYS_REFCURSOR AS
    cursor_resultado SYS_REFCURSOR;
BEGIN
    OPEN cursor_resultado FOR
        SELECT C.NOMBRE AS CURSO, PE.SECCION AS SECCION
        FROM PROFESOR_ESTUDIANTE PE
        INNER JOIN CURSO C ON PE.CODIGO_ESTUDIANTE = C.CODIGO_CURSO
        WHERE PE.ID_PROFESOR = profesor_id;
    RETURN cursor_resultado;
END;


CREATE OR REPLACE PROCEDURE Mostrar_Trabajos_Curso (
    profesor_id IN INT,
    curso_id IN INT
) AS
BEGIN
    -- Ejecutamos la consulta directamente en el ciclo FOR
    FOR rec IN (
        SELECT TI.TITULO AS TITULO_TRABAJO,
               TI.OBJETIVO AS OBJETIVO_TRABAJO,
               TI.PERIODO AS PERIODO_TRABAJO
        FROM TRABAJO_INVEST TI
        INNER JOIN GRUPO G ON TI.NUMERO_GRUPO = G.NUMERO_GRUPO
        INNER JOIN ESTUDIANTE E ON G.NUMERO_GRUPO = E.NUMERO_GRUPO
        INNER JOIN PROFESOR_ESTUDIANTE PE ON E.CODIGO_ESTUDIANTE = PE.CODIGO_ESTUDIANTE
        INNER JOIN CURSO C ON TI.CODIGO_CURSO = C.CODIGO_CURSO
        WHERE PE.ID_PROFESOR = profesor_id
          AND C.CODIGO_CURSO = curso_id
    ) LOOP
        -- Imprime los resultados de la consulta
        DBMS_OUTPUT.PUT_LINE('Título: ' || rec.TITULO_TRABAJO || ', Objetivo: ' || rec.OBJETIVO_TRABAJO || ', Período: ' || rec.PERIODO_TRABAJO);
    END LOOP;
END;

CREATE OR REPLACE PROCEDURE Mostrar_Propuestas (
    profesor_id IN INT,
    curso_id IN INT
) AS
BEGIN
    -- Mostrar las propuestas
    FOR propuesta IN (
        SELECT P.TEMA, P.DESCRIPCION, TI.TITULO AS TITULO_TRABAJO
        FROM PROPUESTA P
        INNER JOIN TRABAJO_INVEST TI ON P.ID_TRABAJO = TI.ID_TRABAJO
        INNER JOIN GRUPO G ON TI.NUMERO_GRUPO = G.NUMERO_GRUPO
        INNER JOIN ESTUDIANTE E ON G.NUMERO_GRUPO = E.NUMERO_GRUPO
        INNER JOIN PROFESOR_ESTUDIANTE PE ON E.CODIGO_ESTUDIANTE = PE.CODIGO_ESTUDIANTE
        WHERE PE.ID_PROFESOR = profesor_id
          AND TI.CODIGO_CURSO = curso_id
    ) LOOP
        DBMS_OUTPUT.PUT_LINE('Propuesta: ' || propuesta.TEMA || ', Descripción: ' || propuesta.DESCRIPCION || ', Título del Trabajo: ' || propuesta.TITULO_TRABAJO);
    END LOOP;
END;

CREATE OR REPLACE PROCEDURE Mostrar_Avances (
    profesor_id IN INT,
    curso_id IN INT
) AS
BEGIN
    -- Mostrar los avances
    FOR avance IN (
        SELECT A.FECHA_ENTREGA, A.FECHA_REVISION, A.NOTA, A.ESTADO, A.COMENTARIO, TI.TITULO AS TITULO_TRABAJO
        FROM AVANCE A
        INNER JOIN TRABAJO_INVEST TI ON A.ID_TRABAJO = TI.ID_TRABAJO
        INNER JOIN GRUPO G ON TI.NUMERO_GRUPO = G.NUMERO_GRUPO
        INNER JOIN ESTUDIANTE E ON G.NUMERO_GRUPO = E.NUMERO_GRUPO
        INNER JOIN PROFESOR_ESTUDIANTE PE ON E.CODIGO_ESTUDIANTE = PE.CODIGO_ESTUDIANTE
        WHERE PE.ID_PROFESOR = profesor_id
          AND TI.CODIGO_CURSO = curso_id
    ) LOOP
        DBMS_OUTPUT.PUT_LINE('Avance - Fecha de Entrega: ' || avance.FECHA_ENTREGA || ', Fecha de Revisión: ' || avance.FECHA_REVISION || ', Nota: ' || avance.NOTA || ', Estado: ' || avance.ESTADO || ', Comentario: ' || avance.COMENTARIO || ', Título del Trabajo: ' || avance.TITULO_TRABAJO);
    END LOOP;
END;
